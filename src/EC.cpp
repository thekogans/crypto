            struct EllipticCurve {
                const util::ui8 *p;
                util::i32 pLength;
                const util::ui8 *a;
                util::i32 aLength;
                const util::ui8 *b;
                util::i32 bLength;
                const util::ui8 *gx;
                util::i32 gxLength;
                const util::ui8 *gy;
                util::i32 gyLength;
                const util::ui8 *n;
                util::i32 nLength;
                const util::ui8 *c;
                util::i32 cLength;
            };

            Params::Ptr ParamsFromEllipticCurve (
                    const EllipticCurve &curve,
                    const ID &id,
                    const std::string &name,
                    const std::string &description) {
                BIGNUMPtr p (BN_new ());
                BIGNUMPtr a (BN_new ());
                BIGNUMPtr b (BN_new ());
                BIGNUMPtr gx (BN_new ());
                BIGNUMPtr gy (BN_new ());
                BIGNUMPtr n (BN_new ());
                BIGNUMPtr c (BN_new ());
                if (p.get () != 0 &&
                        a.get () != 0 && b.get () != 0 &&
                        gx.get () != 0 && gy.get () != 0 &&
                        n.get () != 0 &&
                        c.get () != 0) {
                    BN_bin2bn (curve.p, curve.pLength, p.get ());
                    BN_bin2bn (curve.a, curve.aLength, a.get ());
                    BN_bin2bn (curve.b, curve.bLength, b.get ());
                    BN_bin2bn (curve.gx, curve.gxLength, gx.get ());
                    BN_bin2bn (curve.gy, curve.gyLength, gy.get ());
                    BN_bin2bn (curve.n, curve.nLength, n.get ());
                    BN_bin2bn (curve.c, curve.cLength, c.get ());
                    return EC::ParamsFromGFpCurve (*p, *a, *b, *gx, *gy, *n, *c, id, name, description);
                }
                else {
                    THEKOGANS_CRYPTO_THROW_OPENSSL_EXCEPTION;
                }
            }

